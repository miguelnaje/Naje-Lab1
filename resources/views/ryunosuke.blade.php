            has a lot to improve on, but instead of letting those things bother him, he gets fired up and keeps trying no 
            matter what. However, even for Tanaka, things can be too much, and every six months or so, he starts to think 
            he is nothing but mediocre, but he tells himself that thinking like that is just a waste of time.
