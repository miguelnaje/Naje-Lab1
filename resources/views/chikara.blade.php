            stabilizing presence for the underclassmen; this trait makes him a good candidate to be a captain. Despite 
            this, Ennoshita lacks confidence in himself, mostly because of his lack of experience in matches, and 
            initially had trouble while subbing in for Daichi in the match against Wakutani Minami. Ennoshita's also 
            smart and has never received a failing mark before.
