            first year at Karasuno, Daichi naturally emerged as a leader among him, Asahi, and Sugawara. He would suggest 
            ways for improvement as well as recruiting Kiyoko as a manager even though the Karasuno team was left in 
            disarray without a coach or a strong leader figure. Daichi occasionally has moments where he 
            displays childish behavior, such as when he got in trouble for accidentally setting off the emergency bell 
            while struggling with the captain of the boys' basketball team during the lunchtime rush.
