            <br><br>
            Takeda has messy black hair and brown eyes along with glasses. He's often seen wearing a green track jacket over a white 
            shirt with a black tie.
            <br><br>
            Takeda is persistent, a trait that's helped him secure practice matches and a coach for the team. He has an optimistic 
            attitude and is rather modest and clumsy. He is polite and respectful of others, as shown by his constant bowing to 
            others to show gratitude and to ask for practice matches or things of that sort. Takeda is generally cheerful but can 
            get very intimidating once he's angered.
            <br><br>
            Despite his modesty, Takeda has demonstrated on multiple occasions that he can be firm with his students and takes his 
            job as a teacher and club advisor seriously. He also frequently delivers uplifting speeches and pep-talks while 
            employing a wide range of analogies and imagery in order to inspire the team; however, he occasionally becomes 
            self-conscious and worried that the concepts behind his speeches might be too confusing for the team to understand 
            (a notion that Daichi quickly dismisses).
